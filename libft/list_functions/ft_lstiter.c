#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(int))
{
	t_list	*current;
	t_list	*next;

	if (lst == NULL)
		return ;
	current = lst;
	while (current != NULL)
	{
		next = current->next;
		f(current-> content);
		current = next;
	}
}
